export function getSiteConfig(): SiteConfig {
  if (_siteConfig) return _siteConfig;
  const userConfig = readYamlFile("config/site.yaml");
  const merged = deepMerge(DEFAULT_SITE_CONFIG, userConfig);
  _siteConfig = merged;
  return merged;
}

